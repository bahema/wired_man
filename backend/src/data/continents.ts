export const getContinentForCountry = (country?: string | null) => {
  if (!country) return null;
  const code = country.trim().toUpperCase();
  return COUNTRY_CONTINENT_MAP[code] || null;
};

export const getCountriesForContinents = (continents: string[]) => {
  const targets = new Set(continents.map((item) => item.trim()).filter(Boolean));
  if (targets.size === 0) return [];
  return Object.entries(COUNTRY_CONTINENT_MAP)
    .filter(([, continent]) => targets.has(continent))
    .map(([code]) => code);
};
